package Chain;

/**
 *
 * @author Luchn1k
 */
public class Data {
    private String params;
    private String url;

    public Data(String url, String params) {
        this.params = params;
        this.url = url;
    }

    public String getParams() {
        return params;
    }

    public void setParams(String params) {
        this.params = params;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
